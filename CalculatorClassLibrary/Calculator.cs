namespace CalculatorClassLibrary;

public class Calculator
{
    public int Add(int x, int y)
    { 
        return x + y; 
    }

    public int Subtract(int x, int y) 
    { 
        return x - y; 
    }

    public int Multupli(int x, int y)
    {  
        return x * y;
    }

    public int Divide(int x, int y)
    {  
        return x / y; 
    }
}
